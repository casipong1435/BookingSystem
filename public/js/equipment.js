$(document).on('click', '#chair-check', function(){
  if ($('#chair-check').is(':checked')){
    $('#chair_input').attr('type', 'number');
    $('#chair_input').attr('disabled', false);
              
  }else{
    $('#chair_input').attr('type', 'hidden');
    $('#chair_input').attr('disabled', true);
                
  } 
});

